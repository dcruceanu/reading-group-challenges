<?php


namespace PlaygroundBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Class Table
 *
 * @package PlaygroundBundle\Entity
 * @author  Cruceanu Daniela <daniela.cruceanu@cegeka.com>
 *
 * @ORM\Entity(repositoryClass="PlaygroundBundle\Repository\TableRepository")
 * @ORM\Table(name="table")
 */
class Table
{
    /**
     * @var integer
     *
     * @ORM\Id
     * @ORM\Column(type="integer")
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=260)
     */
    protected $description;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     *
     * @return $this
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * @return string
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * @param string $description
     *
     * @return $this
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }
}
